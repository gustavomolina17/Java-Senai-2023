/* Laços de repetição são utilizados para executar um bloco de código uma
 * determinada quantidade de vezes, ou até que uma condição seja satisfeita.
  
* No Java temos 3 laços de repetição: FOR, WHILE, DO WHILE

1-) FOR --> PARA
Usado para executar o código determinada quantidade de vezes.

*/

public class Laço {

    public static void main(String[] args) {
        for(int i  = 1; i<11; i++ ){
            System.out.println("Valor de i= " +i);
        }
    }
    
}
